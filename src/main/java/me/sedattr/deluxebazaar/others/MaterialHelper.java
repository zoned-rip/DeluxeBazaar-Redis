    private static final MaterialHelper[] VALUES = values();
    private static final Map<String, MaterialHelper> NAMES = new HashMap<>();
    private static final Map<String, MaterialHelper> NAME_CACHE = new HashMap<>();
    private final Material material;
    private final String[] legacy;

    static {
        for (MaterialHelper material : VALUES)
            NAMES.put(material.name(), material);
    }

    MaterialHelper(String... legacy) {
        this.legacy = legacy;

        Material mat = Material.getMaterial(this.name());
        if (mat == null) {
            for (int i = legacy.length - 1; i >= 0; i--) {
                mat = Material.getMaterial(legacy[i]);
                if (mat != null)
                    break;
            }
        }

        this.material = mat;
    }

    private boolean anyMatchLegacy(String name) {
        for (int i = this.legacy.length - 1; i >= 0; i--) {
            if (name.equals(this.legacy[i]))
                return true;
        }

        return false;
    }

    public static Material getMaterial(String name) {
        if (name == null || name.isEmpty())
            return null;
        name = name.toUpperCase();

        MaterialHelper nameCache = NAME_CACHE.getOrDefault(name, null);
        if (nameCache != null)
            return nameCache.parseMaterial();

        Material material = Material.getMaterial(name);
        if (material != null)
            return material;

        material = Material.getMaterial(name.replace("_ITEM", ""));
        if (material != null)
            return material;

        MaterialHelper names = NAMES.getOrDefault(name, null);
        if (names != null) {
            material = names.parseMaterial();
            if (material != null)
                return material;
        }

        for (MaterialHelper materialHelper : VALUES) {
            if (materialHelper.anyMatchLegacy(name)) {
                material = materialHelper.parseMaterial();
                if (material != null) {
                    NAME_CACHE.put(name, materialHelper);
                    return materialHelper.parseMaterial();
                }
            }
        }

        return null;
    }

    public Material parseMaterial() {
        return this.material;
    }
